/**
 * Get a random selection of duas.
 * @param {number} count - Number of duas to return
 * @param {string} [category] - Optional category filter
 */
export function getRandomDuas(count = 3, category = null) {
    let pool = category ? DUAS.filter((d) => d.category === category) : DUAS;
    const shuffled = [...pool].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(count, shuffled.length));
}

/**
 * Get duas by category.
 */
export function getDuasByCategory(category) {
    return DUAS.filter((d) => d.category === category);
}

