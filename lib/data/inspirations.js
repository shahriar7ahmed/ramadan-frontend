/**
 * Get a random inspiration that's different each visit (uses session seed).
 * @returns {Object} A random inspiration object
 */
export function getRandomInspiration() {
    // Use a simple session-based seed so it changes per visit but stays consistent within a session
    const sessionSeed = typeof window !== "undefined"
        ? sessionStorage.getItem("ramadan-inspiration-seed") || (() => {
            const seed = Math.random().toString(36);
            sessionStorage.setItem("ramadan-inspiration-seed", seed);
            return seed;
        })()
        : Math.random().toString(36);

    // Simple hash to get index
    let hash = 0;
    for (let i = 0; i < sessionSeed.length; i++) {
        hash = ((hash << 5) - hash) + sessionSeed.charCodeAt(i);
        hash |= 0;
    }

    const index = Math.abs(hash) % INSPIRATIONS.length;
    return INSPIRATIONS[index];
}

/**
 * Get multiple unique random inspirations.
 */
export function getMultipleInspirations(count = 2) {
    const shuffled = [...INSPIRATIONS].sort(() => Math.random() - 0.5);
    return shuffled.slice(0, Math.min(count, INSPIRATIONS.length));
}
